export const getLocationById = (id: string): Facility | undefined =>
  locations.find(location => location.id === id);

export const getFeaturedLocations = (): Facility[] =>
  locations.filter(location => location.is_partner === true);

export const searchLocations = (query: string): Facility[] => {
  const q = query.toLowerCase();
  return locations.filter(location =>
    location.name.toLowerCase().includes(q) ||
    location.city.toLowerCase().includes(q) ||
    location.zip.includes(q)
  );
};

